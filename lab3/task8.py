def solve(items, capacities):
    c1, c2, c3 = capacities

    dp = {(0, 0, 0): 0}
    parent = {}

    for idx, (name, w, v) in enumerate(items):
        new_dp = dp.copy()

        for (w1, w2, w3), val in dp.items():

            # рюкзак 1
            if w1 + w <= c1:
                state = (w1 + w, w2, w3)
                if state not in new_dp or new_dp[state] < val + v:
                    new_dp[state] = val + v
                    parent[(idx, state)] = (w1, w2, w3, 1)

            # рюкзак 2
            if w2 + w <= c2:
                state = (w1, w2 + w, w3)
                if state not in new_dp or new_dp[state] < val + v:
                    new_dp[state] = val + v
                    parent[(idx, state)] = (w1, w2, w3, 2)

            # рюкзак 3
            if w3 + w <= c3:
                state = (w1, w2, w3 + w)
                if state not in new_dp or new_dp[state] < val + v:
                    new_dp[state] = val + v
                    parent[(idx, state)] = (w1, w2, w3, 3)

        dp = new_dp

    best_state = max(dp, key=lambda x: dp[x])
    best_value = dp[best_state]

    bags = {1: [], 2: [], 3: []}
    state = best_state

    for idx in reversed(range(len(items))):
        key = (idx, state)
        if key in parent:
            prev_w1, prev_w2, prev_w3, bag = parent[key]
            bags[bag].append(items[idx][0])
            state = (prev_w1, prev_w2, prev_w3)

    return best_value, bags


